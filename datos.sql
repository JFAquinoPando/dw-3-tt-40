CREATE DATABASE IF NOT EXISTS proyecto2025;
use proyecto2025;
CREATE TABLE IF NOT EXISTS usuarios(
    id int primary key,
    campo1 varchar(255),
    campo2 varchar(255),
    campo3 varchar(255)
);
CREATE TABLE IF NOT EXISTS productos(
    id int primary key,
    campo1 varchar(255),
    campo2 varchar(255),
    campo3 varchar(255)
);
CREATE TABLE IF NOT EXISTS clientes(
    id int primary key,
    campo1 varchar(255),
    campo2 varchar(255),
    campo3 varchar(255)
);